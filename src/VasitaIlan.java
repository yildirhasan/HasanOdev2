public class VasitaIlan extends Temelilan{

    private String kazaliMi;
    private String sahibindenMi;
    private String  renk;
    private String vitestipi;


    VasitaIlan() {
        super();

    }

    public String getKazaliMi() {
        return kazaliMi;
    }

    public void setKazaliMi(String kazaliMi) {
        this.kazaliMi = kazaliMi;
    }

    public String getSahibindenMi() {
        return sahibindenMi;
    }

    public void setSahibindenMi(String sahibindenMi) {
        this.sahibindenMi = sahibindenMi;
    }

    public String getRenk() {
        return renk;
    }

    public void setRenk(String renk) {
        this.renk = renk;
    }

    public String getVitestipi() {
        return vitestipi;
    }

    public void setVitestipi(String vitestipi) {
        this.vitestipi = vitestipi;
    }
}
